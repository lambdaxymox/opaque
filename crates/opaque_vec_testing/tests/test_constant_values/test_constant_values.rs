use core::ops;

use opaque_vec_testing as ovt;

fn run_test_array_constant<T, const N: usize>(constant: T, expected: [T; N])
where
    T: any::Any + PartialEq + Clone + Default + fmt::Debug,
{
    let result = ovt::constant_values(ovt::ConstantValuesSpec::new(constant));

    assert_eq!(result, expected);
}

macro_rules! generate_tests {
    ($typ:ident, $( $test_name:ident :: $array_length:expr => $constant_value:expr => $expected:expr ),* $(,)?) => {
        mod $typ {
            $(
                #[test]
                fn $test_name() {
                    let constant_value: $typ = $constant_value;
                    let expected: [$typ; $array_length] = $expected;
                    super::run_test_array_constant::<$typ, $array_length>(constant_value, expected);
                }
            )*
        }
    };
}

